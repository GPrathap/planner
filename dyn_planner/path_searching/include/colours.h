#include <cmath>


struct ColorRGBA{
  float r;
  float g;
  float b;
  float a;
};

inline ColorRGBA heightMapColor(float h) {

  ColorRGBA color;
  
  // blend over HSV-values (more colors)
  float s = 1.0;
  float v = 1.0;

  h -= std::floor(h);
  h *= 6;
  int i;
  float m, n, f;

  i = std::floor(h);
  f = h - i;
  if (!(i & 1))
    f = 1 - f; // if i is even
  m = v * (1 - s);
  n = v * (1 - s * f);

  switch (i) {
    case 6:
    case 0:
      color.r = v; color.g = n; color.b = m;
      break;
    case 1:
      color.r = n; color.g = v; color.b = m;
      break;
    case 2:
      color.r = m; color.g = v; color.b = n;
      break;
    case 3:
      color.r = m; color.g = n; color.b = v;
      break;
    case 4:
      color.r = n; color.g = m; color.b = v;
      break;
    case 5:
      color.r = v; color.g = m; color.b = n;
      break;
    default:
      color.r = 1; color.g = 0.5; color.b = 0.5;
      break;
  }
  return color;
}


